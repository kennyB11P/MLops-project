def find_labels_in_text(text: str) -> list[str]:
    """Находит labels по простым ключевым словам в пользовательском запросе."""
    lowered = text.lower()
    labels: list[str] = []

    for keyword, label in LABEL_ALIASES.items():
        if keyword in lowered and label not in labels:
            labels.append(label)

    return labels
